    # 数学一
    for year, image_ids in math1_data.items():
        year_dir = os.path.join(target_dir, '数学一', f'{year}年')
        for i, img_id in enumerate(image_ids):
            src_file = os.path.join(answer_dir, f'{img_id}.png')
            if os.path.exists(src_file):
                dst_file = os.path.join(year_dir, f'{i+1:02d}_{img_id}.png')
